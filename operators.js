// Logical operators always validate to a boolean value ei true or false
// && - and: both sides of the operator must be true (And Gate)
// 2 && 2= true
// true && true = true
// true && false = false
// false && true = false
// false && false = false

// || - or
// at least one side of the operator must be true (Or Gate)
// true || true = true
// true || false = true
// false || true = true
// false || false = false


// ! - not
// negates the value of a boolean
// !true = false
// !false = true



// Comparison operators
// is used to compare two values and always returns a boolean value
// 2 == 2 // true
// == - equal to: checks only value
console.log(2 == '2') // true
// === - strict or absolute equal to (value and type) : checks type and value
console.log(2 === '2') // false

// != - not equal to
// > - greater than
// < - less than
// >= - greater than or equal to
// 2 >= 1 // true
// <= - less than or equal to

// Assignment operators
// = - assign value
let a = 2;
// += - add and assign value
a += 2;
// -= - subtract and assign value
// *= - multiply and assign value
// /= - divide and assign value

// Increment and decrement operators
// ++ - increment
// -- - decrement
let b = 2;
b++; // b = b + 1


